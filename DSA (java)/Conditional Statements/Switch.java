public class Switch {
    public static void main(String[] args) {
        int number = 2;
        switch (number) {
            case 3:
                System.out.println("samosa");
                break;
            case 2:
                System.out.println("Mango Shake");
                break;
            case 1:
                System.out.println("Burger");
                break;
            default:
                System.out.println("We wake up");
        }
    }
}